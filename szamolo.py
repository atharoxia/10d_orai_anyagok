#Olvassuk be két számot, és írjuk ki az összegüket:
szam1 = int(input("Egyik szám: "))
szam2 = int(input("Másik szám: "))
osszeg = szam1 + szam2
szorzat = szam1 * szam2
kulonbseg = szam1 - szam2
hanyados = szam1 / szam2
egeszosztas = szam1 // szam2
maradek = szam1 % szam2
hatvany = szam1 ** szam2

print("A két szám összege ", osszeg)
print("A két szám szorzata ", szorzat)
print("A két szám különbsége ", kulonbseg)
print("A két szám hányadosa ", hanyados)
print("A két szám egészhányadosa ", egeszosztas)
print("A két szám osztási maradéka ", maradek)
print("Az első szám a másodikra emelve ", hatvany)

